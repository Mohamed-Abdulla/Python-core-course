
#!Bitwise operations
#!complemet- ~
#!and &
#!or |
#!xor ^
#!left shift- <<
#!right shift->>

#!complemet- ~ (tilde)
print(~122) # it uses 2's complement(1's complement +1) -123
#!and &
print(12&13) #12
#!or |
print(12|13) #12
#!xor ^ (both same=0,or 1)
print(12^13)
#!left shift- <<
print(10<<2) #1010 - add 2 0s after 1010->101000 
#!right shift->> 
print(10>>2) #1010 -removind 2 values 1010->10 



